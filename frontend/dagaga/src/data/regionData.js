/**
 * 선택된 시/도와 구/군을 location_id로 변환
 * @param {string} sido - 시/도 이름 (예: "서울특별시")
 * @param {string} gugun - 구/군 이름 (예: "강남구")
 * @returns {number|null} location_id 또는 null
 */
export function getLocationId(sido, gugun) {
    // 시/도가 선택되지 않았거나 기본값인 경우
    if (!sido || sido === "시/도 선택") {
        return null;
    }
    
    // 구/군이 선택되지 않았거나 기본값인 경우
    if (!gugun || gugun === "구/군 선택") {
        // 시/도만 선택된 경우 시/도의 location_id 반환
        return sidoToLocationId[sido] || null;
    }
    
    // 구/군이 선택된 경우
    const gugunMap = gugunToLocationId[sido];
    if (gugunMap && gugunMap[gugun]) {
        return gugunMap[gugun];
    }
    
// 매핑을 찾지 못한 경우 null 반환
    return null;
}

/**
 * location_id로 지역 이름 찾기 check
 * @param {number} id - location_id
 * @returns {string} 지역 이름 (예: "서울특별시 강남구")
 */
export function getLocationName(id) {
    if (!id) return '';

    // 1. 구/군에서 찾기 (Depth 2)
    for (const [sido, guguns] of Object.entries(gugunToLocationId)) {
        for (const [gugun, locationId] of Object.entries(guguns)) {
            if (locationId === id) {
                return `${sido} ${gugun}`;
            }
        }
    }

    // 2. 시/도에서 찾기 (Depth 1)
    for (const [sido, locationId] of Object.entries(sidoToLocationId)) {
        if (locationId === id) {
            return sido;
        }
    }

    return '알 수 없는 지역';
}
